// itch.io bootstrap: run the SAME server engine in the browser.
//
// itch.io serves an HTML5 game as static files inside a sandboxed iframe: there
// is no server. So instead of a second, hand-maintained JavaScript engine (which
// would drift out of sync with the Python), this loads Pyodide, installs Flask,
// unpacks the real runtime (app.py / hallway.py / anomalies.py / memory.py /
// i18n.py + the arc data + compiled i18n) into Pyodide's filesystem, boots the
// exact same Flask app, and dispatches /api/* through Flask's test client. The
// client (game.js) is byte-for-byte the one the server ships; it just talks to a
// local Flask instead of a remote one. One engine, two deploy targets, always in
// sync.
//
// This file is loaded ONLY by the itch build's static index.html (generated by
// scripts/build_itch.py). On the Hugging Face / Flask deploy it is never
// referenced, so the server path is completely untouched.
(function () {
  "use strict";

  // Pin a known-good Pyodide. Overridable without editing this file: set
  // window.H8_PYODIDE_VER (or a full window.H8_PYODIDE_BASE) in index.html, so
  // you can bump to the current release and re-test in a real browser (the
  // sandbox that builds this has no outbound network to load Pyodide itself).
  var PYODIDE_VER = window.H8_PYODIDE_VER || "0.26.4";
  var PYODIDE_BASE =
    window.H8_PYODIDE_BASE ||
    "https://cdn.jsdelivr.net/pyodide/v" + PYODIDE_VER + "/full/";

  var origFetch = window.fetch.bind(window);
  var dispatch = null; // set once the Python engine is booted
  var booting = false;
  var readyResolve, readyReject;
  var ready = new Promise(function (res, rej) {
    readyResolve = res;
    readyReject = rej;
  });

  // ---- fetch shim ---------------------------------------------------------
  // game.js issues three kinds of request:
  //   /api/* and /healthz  -> the server; route to the in-browser Flask.
  //   /static/*            -> absolute path; on itch the game lives under a
  //                           sub-path, so rewrite to a relative path.
  //   everything else      -> pass straight through.
  window.fetch = function (input, init) {
    var url = typeof input === "string" ? input : (input && input.url) || "";
    var u;
    try {
      u = new URL(url, location.href);
    } catch (e) {
      return origFetch(input, init);
    }
    var path = u.pathname;
    if (path.indexOf("/static/") === 0) {
      return origFetch("static/" + path.slice("/static/".length) + u.search, init);
    }
    if (path.indexOf("/api/") === 0 || path === "/healthz") {
      return handleApi(path + u.search, init);
    }
    return origFetch(input, init);
  };

  function toPlainHeaders(h) {
    var out = {};
    if (!h) return out;
    if (typeof h.forEach === "function" && !Array.isArray(h)) {
      // Headers instance
      h.forEach(function (v, k) {
        out[k] = v;
      });
      return out;
    }
    if (Array.isArray(h)) {
      h.forEach(function (pair) {
        out[pair[0]] = pair[1];
      });
      return out;
    }
    for (var k in h) {
      if (Object.prototype.hasOwnProperty.call(h, k)) out[k] = h[k];
    }
    return out;
  }

  async function handleApi(fullpath, init) {
    if (!booting) {
      booting = true;
      boot();
    }
    try {
      await ready;
    } catch (e) {
      return jsonResponse({ error: "engine failed to load" }, 503);
    }
    var method = ((init && init.method) || "GET").toUpperCase();
    var headers = toPlainHeaders(init && init.headers);
    var body = init && init.body != null ? init.body : null;
    if (body != null && typeof body !== "string") body = String(body);
    var out;
    try {
      var raw = dispatch(fullpath, method, JSON.stringify(headers), body);
      out = JSON.parse(raw);
    } catch (e) {
      return jsonResponse({ error: String(e) }, 500);
    }
    return new Response(out.body, {
      status: out.status,
      headers: { "Content-Type": out.ct || "application/json" },
    });
  }

  function jsonResponse(obj, status) {
    return new Response(JSON.stringify(obj), {
      status: status,
      headers: { "Content-Type": "application/json" },
    });
  }

  // ---- boot ---------------------------------------------------------------
  var BOOT_PY = [
    "import os, sys, json",
    "os.chdir('/app')",
    "if '/app' not in sys.path: sys.path.insert(0, '/app')",
    "import app as _app",
    "_client = _app.app.test_client()",
    "def h8_dispatch(path, method, headers_json, body):",
    "    headers = json.loads(headers_json) if headers_json else {}",
    "    kwargs = {'method': method, 'headers': headers}",
    "    if body is not None and body != '':",
    "        kwargs['data'] = body",
    "        headers.setdefault('Content-Type', 'application/json')",
    "    r = _client.open(path, **kwargs)",
    "    return json.dumps({'status': r.status_code,",
    "                       'body': r.get_data(as_text=True),",
    "                       'ct': r.headers.get('Content-Type', 'application/json')})",
    "",
  ].join("\n");

  function loadScript(src) {
    return new Promise(function (res, rej) {
      var s = document.createElement("script");
      s.src = src;
      s.onload = res;
      s.onerror = function () {
        rej(new Error("failed to load " + src));
      };
      document.head.appendChild(s);
    });
  }

  async function boot() {
    showStatus();
    try {
      await loadScript(PYODIDE_BASE + "pyodide.js");
      var pyodide = await loadPyodide({ indexURL: PYODIDE_BASE });
      await pyodide.loadPackage("micropip");
      var micropip = pyodide.pyimport("micropip");
      await micropip.install("flask");
      var buf = await (await origFetch("app.tar")).arrayBuffer();
      try {
        pyodide.FS.mkdir("/app");
      } catch (e) {
        /* already there */
      }
      pyodide.unpackArchive(new Uint8Array(buf), "tar", { extractDir: "/app" });
      await pyodide.runPythonAsync(BOOT_PY);
      dispatch = pyodide.globals.get("h8_dispatch");
      hideStatus();
      readyResolve();
    } catch (e) {
      console.error("[itch-boot] engine failed to load:", e);
      showStatus(true);
      readyReject(e);
    }
  }

  // A whisper of a loading line, matched to the game's hush. Removed the moment
  // the engine is live (the landing's title/lead paint instantly regardless).
  var statusEl = null;
  function showStatus(failed) {
    if (!statusEl) {
      statusEl = document.createElement("div");
      statusEl.setAttribute("aria-live", "polite");
      statusEl.style.cssText =
        "position:fixed;left:0;right:0;bottom:1.1rem;text-align:center;" +
        "font:400 0.72rem/1.4 system-ui,sans-serif;letter-spacing:.02em;" +
        "opacity:.34;pointer-events:none;z-index:9;color:currentColor;" +
        "transition:opacity .6s ease";
      document.addEventListener("DOMContentLoaded", function () {
        if (statusEl && !statusEl.isConnected) document.body.appendChild(statusEl);
      });
      if (document.body) document.body.appendChild(statusEl);
    }
    statusEl.textContent = failed
      ? "Could not reach the engine. Check your connection and reload."
      : "settling in…";
    statusEl.style.opacity = failed ? ".6" : ".34";
  }
  function hideStatus() {
    if (!statusEl) return;
    statusEl.style.opacity = "0";
    setTimeout(function () {
      if (statusEl && statusEl.parentNode) statusEl.parentNode.removeChild(statusEl);
      statusEl = null;
    }, 700);
  }
})();
